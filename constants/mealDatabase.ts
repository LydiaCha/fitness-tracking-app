const _mealMap = new Map(MEAL_DATABASE.map(m => [m.id, m]));

export function getMeal(id: string): MealRecord | undefined {
  return _mealMap.get(id);
}

export function getMealsByType(type: import('@/types/meal').MealType): MealRecord[] {
  return MEAL_DATABASE.filter(m => m.mealType.includes(type));
}
